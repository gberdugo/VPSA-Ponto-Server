package br.berdugo.vpsa.pojo.registro;

public class RegistroRelatorioAnaliticoHorasPojo {

	private Integer diaMes;
	
	private String diaSemana;
	
	private String horaEntrada;
	
	private String horaSaidaAlmoco;
	
	private String horaEntradaAlmoco;
	
	private String horaSaida;
	
	private String totalHoras;
	
	private String totalAdicional;
	
	public Integer getDiaMes() {
		return diaMes;
	}
	
	public void setDiaMes(Integer diaMes) {
		this.diaMes = diaMes;
	}
	
	public String getDiaSemana() {
		return diaSemana;
	}
	
	public void setDiaSemana(String diaSemana) {
		this.diaSemana = diaSemana;
	}
	
	public String getHoraEntrada() {
		return horaEntrada;
	}
	
	public void setHoraEntrada(String horaEntrada) {
		this.horaEntrada = horaEntrada;
	}
	
	public String getHoraSaidaAlmoco() {
		return horaSaidaAlmoco;
	}
	
	public void setHoraSaidaAlmoco(String horaSaidaAlmoco) {
		this.horaSaidaAlmoco = horaSaidaAlmoco;
	}
	
	public String getHoraEntradaAlmoco() {
		return horaEntradaAlmoco;
	}
	
	public void setHoraEntradaAlmoco(String horaEntradaAlmoco) {
		this.horaEntradaAlmoco = horaEntradaAlmoco;
	}
	
	public String getHoraSaida() {
		return horaSaida;
	}
	
	public void setHoraSaida(String horaSaida) {
		this.horaSaida = horaSaida;
	}
	
	public String getTotalAdicional() {
		return totalAdicional;
	}
	
	public void setTotalAdicional(String totalAdicional) {
		this.totalAdicional = totalAdicional;
	}
	
	public String getTotalHoras() {
		return totalHoras;
	}
	
	public void setTotalHoras(String totalHoras) {
		this.totalHoras = totalHoras;
	}
}
