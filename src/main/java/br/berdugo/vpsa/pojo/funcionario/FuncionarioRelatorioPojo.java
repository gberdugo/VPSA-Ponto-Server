package br.berdugo.vpsa.pojo.funcionario;

public class FuncionarioRelatorioPojo {

	private Long id;
	
	private String nome;
	
	private String rfid;
	
	public Long getId() {
		return id;
	}
	
	public void setId(Long id) {
		this.id = id;
	}
	
	public String getNome() {
		return nome;
	}
	
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	public String getRfid() {
		return rfid;
	}
	
	public void setRfid(String rfid) {
		this.rfid = rfid;
	}
}
